nice_name <- function(x) {
  x <- stringi::stri_replace_all_fixed(x, subs[, 1], subs[, 2],
    vectorize_all = FALSE)

  # Clean up any remaining
  x <- str_replace_all(x, "[^A-Za-z0-9_.-]+", "-")
  x <- str_replace_all(x, "-+", "-")
  x <- str_replace_all(x, "^-|-$", "")
  x <- str_replace_all(x, "^\\.", "dot-")
  permitted_length<-72
  if (stringr::str_length(x)>permitted_length){
    # If the filename for the *.Rd  file would get too long
    # to be accepted by cran due to limitations 
    # of the  'tar ' program ( which happens for methods with
    # long signatures )
    # we compute a hash which is shorter but 
    # also unique (at least with with very high probality)
    # and then construct a new name that starts with 
    # the first letters of the original name but 
    # uses the hash for the rest.
    # This way we keep as much of the descriptive part
    # of the filename as possible (in most cases everything).
    x<-paste0(
      substring(x,1,(permitted_length-8)), #the first characters
      digest::digest(as.character(x),algo='crc32')) # the last 8
  }
  x
}

write_if_different <- function(path, contents, check = TRUE) {
  if (!file.exists(dirname(path))) {
    dir.create(dirname(path), showWarnings = FALSE)
  }

  if (check && !made_by_roxygen(path)) {
    warning("The existing '", basename(path),
      "' file was not generated by roxygen2, and will not be overwritten.",
      call. = FALSE, immediate. = TRUE)
    return(FALSE)
  }

  if (same_contents(path, contents)) return(FALSE)

  name <- basename(path)
  if (!str_detect(name, "^[a-zA-Z][a-zA-Z0-9_.-]*$")) {
    cat("Skipping invalid path: ", name, "\n")
    FALSE
  } else {
    cat(sprintf('Writing %s\n', name))
    write_lines(contents, path)
    TRUE
  }
}

same_contents <- function(path, contents) {
  if (!file.exists(path)) return(FALSE)

  contents <- paste0(paste0(contents, collapse = "\n"), "\n")

  text_hash <- digest::digest(contents, serialize = FALSE)
  file_hash <- digest::digest(file = path)

  identical(text_hash, file_hash)
}

compact <- function(x) {
  x[!map_lgl(x, is.null)]
}

# Parse DESCRIPTION into convenient format
read.description <- function(file) {
  dcf <- desc::desc(file = file)

  fields <- dcf$fields()
  purrr::map(purrr::set_names(fields), ~ dcf$get_field(.x))
}

invert <- function(x) {
  if (length(x) == 0) return()
  stacked <- utils::stack(x)
  tapply(as.character(stacked$ind), stacked$values, list)
}

